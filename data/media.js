TrackHTML=function(MediaObj){
	if(MediaObj.TRACK&&MediaObj.MUSIC_LINK)
		return AHTML(MediaObj.TRACK,MediaObj.MUSIC_LINK);
	else
		return MediaObj.TRACK;
}

TrackAuthorHTML=function(MediaObj){
	if(MediaObj.AUTHOR_LINK&&MediaObj.AUTHOR)
		return AHTML(MediaObj.AUTHOR,MediaObj.AUTHOR_LINK);
	else
		return MediaObj.AUTHOR;
}


AudioTrackHMTL=function(track){
	return `<audio class="music" src="media/${PageIdentifier()}/${track.AUTHOR} - ${track.TRACK}.mp3" preload="none"></audio>`;
}

MusicTableRowF=function(headers){
	return function(MediaObj){
		var data=[];
		if(In(headers,"Track"))
			data.push(TrackHTML(MediaObj));

		if(In(headers,"Author"))
			data.push(TrackAuthorHTML(MediaObj));
		
		if(In(headers,"Genre"))
			data.push(MediaObj.GENRE);
	
		if(In(headers,"Year"))
			data.push(MediaObj.YEAR);

		if(In(headers,"License"))
			data.push(MediaObj.LICENSE);
		return data;
	}
}

UniqueHeader=function(tracks,header){
	var values=tracks.map(t=>t[header.toUpperCase()]);
	return Union(values).length>1;
}
PresentHeader=function(tracks,header){
	return In(Keys(tracks[0]),header.toUpperCase());
}
UniqueHeaders=function(tracks,headers){
	return headers.filter(function(h){return UniqueHeader(tracks,h)&&PresentHeader(tracks,h)});
}
CommonHeaders=function(tracks,headers){
	return headers.filter(function(h){return !UniqueHeader(tracks,h)&&PresentHeader(tracks,h)});
}

var MusicHeaders=["Track","Author","Genre","Year","License"];

MusicTableHTML=function(tracks,headers){
	if(tracks.length<=1)
		return "";
	
	var caption=`<em>${v.TITLE()}'s soundtrack playlist:</em>`;
	var rows=tracks.map(MusicTableRowF(headers));
	return TableHTML({caption:caption,headers:headers,rows:rows});
}


MusicCreditsHTML=function(id){
	var tracks=Values(FilterValuesObject(Media,t=>(t.CONTEXT_ID===id&&t.TYPE==="music")));
	tracks=Sorter(a=>a.N)(tracks);
	audiotracks=tracks.map(AudioTrackHMTL).join("\n")

	if(tracks.length<1)
		return "";
	var headers=UniqueHeaders(tracks,MusicHeaders);
	var headersCaption=CommonHeaders(tracks,MusicHeaders);
	
	if(tracks.length==1)
		var table="";
	else
		var table=MusicTableHTML(tracks,headers);	
	
	var Track=In(headersCaption,"Track")?` "${TrackHTML(tracks[0])}"`:"";
	var Author=In(headersCaption,"Author")?` by ${TrackAuthorHTML(tracks[0])}`:"";
	var Genre=In(headersCaption,"Genre")?`(${tracks[0]["GENRE"]})`:"";
	var Year=In(headersCaption,"Year")?`, ${tracks[0]["YEAR"]}`:"";
	var License=In(headersCaption,"License")?`, ${tracks[0]["LICENSE"]}`:"";

	var caption=`<h3>Music</h3><p>Background ${Genre} music ${Track}${Author}${Year}${License}.</p>`

	return caption+table+audiotracks;
}

ImageSource=function(ImageObj){
	var src=ImageObj.src?`images/${ImageObj.src}`:`images/${ImageObj.FOLDER_SMALL||ImageObj.folder||""}/${ImageObj.name||ImageObj.name}`;
		src=SourceCoerceExtension(src,ImageExtensions,"png");
	if(Prefixed(ImageObj.src,"data:"))
		src=ImageObj.src;
	return src
}

ImageHTML=function(ImageObj){
	var ImageObj=ReKeyObject(ImageObj,LowerCase);
	if(!ImageObj.lazy)
		var src=`src="${ImageSource(ImageObj)}"`;
	return `
	<img
		alt="${ImageObj.alt||ImageObj.description||ImageObj.src}" 
		title="${ImageObj.description||ImageObj.title||ImageObj.src}"
		class="image"
		loading="lazy"
		${src}
		id="${ImageObj.id}"
	/>`
}

LazyImageHTML=function(ImageObj){
	var id=GenerateId();
	LazyImageLoader(id,ImageSource(ImageObj));
	return ImageHTML(Merge(ImageObj,{id:id,lazy:true}));
}

I=function(src){
	if(!Posfixed(src,ImageExtensions))
		var src=Posfix(src,".png");
	return LazyImageHTML({src:src});
}

ImageCardHTML=function(ImageObj){
	if(ImageObj.href){
		var link=ImageObj.href;
		var target="";
	}
	else{ //by default, link to image file
		var link=ImageSource(ImageObj);
		var target=v.BLANK()
	}

	var legend=ImageObj.LEGEND?`<div>${ImageObj.LEGEND}</div>`:"";
	var id=ImageObj.id||GenerateId();

	var cardTop=`
	<div class="card ${ImageObj.CLA||""}">
		${LazyImageHTML(ImageObj)}
	</div>
	`;
	return AHTML(cardTop,link,{target:target,id:id,class:"card-supra underborderable"})+legend;
}

LaunchImageModal=function(subfolder,title){
	var ImageObj=ImageObject({
		folder:subfolder,
		name:title
	});
	OpenModal(ImageHTML(ImageObj))
}



ScreenshotGalleryHTML=function(id){
	var screenshots=Values(FilterValuesObject(Media,t=>(t.CONTEXT_ID===id&&t.TYPE==="screenshot")));
	if(screenshots.length<1)
		return "";
	var gallery=screenshots.map(ImageCardHTML).join("\n");
	return `
	<h3 class="images">Screenshots</h3>
	<p>Download <a class="download" title="Download all!" href="images/${v.LINK()}/${v.LINK()}-screenshots.zip">all screenshots (zip archive)</a>.</p>
	<div class="featured">${gallery}</div>`
}

function ImageObject(opts){
	var opts=opts||{};
	var name=opts.name;
	var folder=opts.folder;
	return Merge({id:name},opts,{
		FOLDER_SMALL:folder,
		DESCRIPTION:name,
		TRACK:name});
};

GalleryHTML=function(objects,Renderer){
	var gallery=objects.map(Renderer).join("\n");
	return `<div class="featured">${gallery}</div>`
}


FolderGalleryHTML=function(subfolder,names,ObjectRenderer){
	if(IsArray(names)){
		if(names.length<1)
			return "";
		var objects=names.map(name=>ImageObject({name:name,folder:subfolder}));
	}
	else if(IsObject(names)){
		var objects=Keys(names).map(n=>ImageObject(Join(names[n],{name:n,folder:subfolder})));
	}
	
	return GalleryHTML(objects,ObjectRenderer||ImageCardHTML);
}

ModalImageFragment=function(obj){
	var	frag=PageFragment();
	if(!frag)
		return;
	var folder=PageIdentifier();
	function Simplify(sourcefragment){
		return UnPosfix(sourcefragment,ImageExtensions).replace(folder,"");
	}
	var focuses=Keys(obj).filter(k=>In(Simplify(k),Simplify(frag)));
	if(focuses.length)
		LaunchImageModal(folder,First(focuses))
}



