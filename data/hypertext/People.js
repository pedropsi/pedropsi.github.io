var CommonURLS={
	GOOGLE_PLUS:`https://plus.google.com/u/0/`,
	TWITTER:`https://twitter.com/`,
	GITHUB_IO:`.github.io/`,
	GITHUB_COM:`.github.com/`,
	ITCH:`.itch.io/`
}

PersonalPage=function(linkObj){
	if(linkObj.ITCH)
		return `https://${linkObj.ITCH}${CommonURLS.ITCH}`;
	if(linkObj.TWITTER)
		return `${CommonURLS.TWITTER}${linkObj.TWITTER}`;
	if(linkObj.GITHUB_IO)
		return `https://${linkObj.GITHUB_IO}${CommonURLS.GITHUB_IO}`;
	if(linkObj.GITHUB_COM)
		return `https://${linkObj.GITHUB_COM}${CommonURLS.GITHUB_COM}`;
	if(linkObj.PERSONAL_PAGE)
		return `${linkObj.PERSONAL_PAGE}`;
	if(linkObj.GOOGLE_PLUS)
		return `${CommonURLS.GOOGLE_PLUS}${linkObj.GOOGLE_PLUS}`;
	if(linkObj.TWITCH)
		return `https://www.twitch.tv/${linkObj.TWITCH}`
	else
		return "";
}
	
NameAliasString=function(linkObj){
	var name=Evaluate(linkObj.name);
	var alias=Evaluate(linkObj.alias||"");
	
	if(!name&&!alias)
		return console.log("missing!");
	if(!name&&alias)
		return alias;
	
	alias=alias?` (${alias})`:"";
	return `${name}${alias}`;
}

var PersonAliases={};

PersonHTML=function(linkObj,key){
	var page=PersonalPage(linkObj);
	var name=NameAliasString(linkObj);
	var element=`<span class="name">${name}</span>`;
	var id=IndexCaseString("p-"+key);
	if(page!=="")
		element=AHTML(element,page,{class:"person "+id,id:id,"data-first":UnAfterfix(name," ")});
	return element;
}

RegisterPerson=function(alias,key){
	var alias=UniformString(alias);
	if(PersonAliases[alias]){
		if(PersonAliases[alias]!==key)
			HyperPerson.ambiguous.push(alias);
	}else{
		PersonAliases[alias]=key;
		HyperText("People/"+alias,()=>PersonHTML(Persons[key],key));
	}
}

MemorableHyperStrings=function(string){
	if(!string)
		return [];
	return string.split(/(\s|\.)+/).concat(string).filter(n=>n.length>2);
}

Keys(Persons).map(function(k){
	RegisterPerson(k,k);
	MemorableHyperStrings(Persons[k].alias||"").map(n=>RegisterPerson(n,k))
	MemorableHyperStrings(Persons[k].name||"").map(n=>RegisterPerson(n,k))
	MemorableHyperStrings(Persons[k].shorthand||"").map(n=>RegisterPerson(n,k))
	MemorableHyperStrings(Persons[k].TWITTER||"").map(n=>RegisterPerson(n,k))
});

PeopleSelfReferencer=function(){
	var peopleElements=GetElements(".person");
	var firstElements=DistinctArray(peopleElements,Classes);
	var referenceElements=UnDistinctArray(peopleElements,Classes);
	referenceElements.map(e=>e.outerHTML=InnerAHTML(e.getAttribute("data-first"),"#"+e.id));
}

