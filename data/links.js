function LinkTemplate(linkObj){
	var title=Unfunctionalise(linkObj.TITLE);
	var author=Unfunctionalise(linkObj.AUTHOR||"");

	var page=Unfunctionalise(linkObj.PAGE||title);
	var ext=Unfunctionalise(linkObj.EXT||"");
	var search=Unfunctionalise(linkObj.SEARCH||"");

	if(ext)
		ext=Prefix(ext,".")

	if(search)
		search=Prefix(search,"?search=");
		
	if(author)
		return `${author}'s ${AHTML(title,page+ext+search)}`;
	else
		return AHTML(title,page+ext);
}

function LinkGroup(GroupObject){
	if(typeof GroupObject==="string")
		return LinkGroup({GROUP:GroupObject});
	return BaseFilter(Links,GroupObject);
}

function LinkGroupHTML(GroupObject){
	var links=LinkGroup(GroupObject).map(g=>`<li>${LinkTemplate(g)},</li>`);
	if(!links.length)
		return "";
	return Posfix(UnPosfix(links.join("\n"),",</li>"),".</li>");
}


