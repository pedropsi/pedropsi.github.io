LinksNormalised=Clone(Links);


LinkTemplate=function(linkObj){
	var title=UnFunction(linkObj.TITLE);
	var author=UnFunction(linkObj.AUTHOR||"");

	var page=UnFunction(linkObj.link||title);
	var ext=UnFunction(linkObj.EXT||"");
	var search=UnFunction(linkObj.SEARCH||"");

	if(ext)
		ext=Prefix(ext,".")

	if(search)
		search=Prefix(search,"?search=");
		
	if(author)
		return `${author}'s ${AHTML(title,page+ext+search)}`;
	else
		return AHTML(title,page+ext);
}

LinkGroup=function(GroupObject){
	if(typeof GroupObject==="string")
		return LinkGroup({GROUP:GroupObject});
	return BaseFilter(LinksNormalised,GroupObject);
}

LinkGroupHTML=function(GroupObject){
	var links=LinkGroup(GroupObject).map(g=>`<li>${LinkTemplate(g)},</li>`);
	if(!links.length)
		return "";
	return Posfix(UnPosfix(links.join("\n"),",</li>"),".</li>");
}



