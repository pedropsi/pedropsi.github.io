PathGrower=function(dx,dy){
	if(!STATE.mode.path||!STATE.mode.path.length)
		STATE.mode.path=[[0,0]];

	var xy=Last(STATE.mode.path);
	var x=Max(Min(xy[0]+dx||0,STATE.W),0);
	var y=Max(Min(xy[1]+dy||0,STATE.H),0);

	if(xy[0]!==x||xy[1]!==y)
		STATE.mode.path.push([x,y]);
	
	return [x,y];
}

