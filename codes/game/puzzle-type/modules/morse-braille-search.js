function MorseFind(string){
	return MorseWords(MorseNext(string)).filter(Iner(EnDictionary()))
}

var MSubstrings={};
function MorseSubstrings(string,max){
	var max=max||12;//16
	if(string.length<=1)
		return ;
	else{
		var rest=Rest(string);
		var most=Most(string);
		if(!In(MSubstrings,rest)){
			MSubstrings[rest]=(rest.length<=max)?MorseFind(rest):"?";
			MorseSubstrings(rest,max);
		}
		if(!In(MSubstrings,most)){
			MSubstrings[most]=(most.length<=max)?MorseFind(most):"?";
			MorseSubstrings(most,max);
		}
	}
	return MSubstrings;
}

function MorseSentence(string){
	var sentence={};
	for(var i=0;i<string.length;i++){
		var start=string.split("").splice(0,i).join("");
		var rest=string.split("").splice(i,string.length-1).join("");
		if(start.length===0&&MSubstrings[rest]&&MSubstrings[rest].length&&MSubstrings[rest]!=="?"){
			MSubstrings[rest].map(word=>sentence[word]=[]);
		}else
		if(MSubstrings[start]&&MSubstrings[start].length&&MSubstrings[start]!=="?"){
			var s=MorseSentence(rest);
			if(Keys(s).length)
				MSubstrings[start].map(word=>sentence[word]=s);
		}
	}
	return sentence;
}

function MorseSearch(string){
	var string=string.replaceAll("/","");
	MorseSubstrings(string);
	return MorseSentence(string);
}

