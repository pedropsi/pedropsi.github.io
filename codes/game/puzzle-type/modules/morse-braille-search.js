function MorseNext(string,letters){
	var next=[];
	var string=string||"";
	var letters=letters||""
	if(!string)
		return [];
	Keys(MorseLatin).map(
		function(m){
			if(Prefixed(string,m)){
				var letter=MorseLatin[m];
				var s=string.split("");
					s.splice(0,m.length);
					s=s.join("");
				if(In(EnPairs,Last(letters)+letter)||letters==="")
					next.push({
						letter:letters+letter,
						string:s,
						next:MorseNext(s,letters+letter)		
					})
			}
		}
	)
	return next;
}

function MorseWords(morsenext){
	return morsenext.map(
		function(o){
			if(!o.next.length)
				return o.letter;
			else
				return MorseWords(o.next).flat();
		}
	).flat();
}

function StringPairs(string){
	var pairs=[];
	for(var i=0;i<(string.length-1)/2;i++)
		pairs.push(string[2*i]+string[2*i+1]);
	for(var j=1;j<(string.length)/2;j++)
		pairs.push(string[2*j-1]+string[2*j]);
	return Unique(pairs);
}

