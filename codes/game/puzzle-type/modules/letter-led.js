function LEDLetterIconHTML(ledstring){
	var coordinates=ledstring.split("");
		coordinates=coordinates.map(c=>LEDCoordinates[c]);
		coordinates=coordinates.join(" ");

	return IconHTML({
		path:coordinates,
		vbmin:"-4 -2",
		vbmax:"22 22",
		transform:"flip-vertical",
		cla:"led letter"
	})
}
