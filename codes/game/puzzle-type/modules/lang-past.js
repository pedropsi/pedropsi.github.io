function RegularPastTense(reg){
	return (reg+"ed").replace(/eed$/,"ed");
}

function PastTense(verb){
	return  In(IrregularVerbPasts,verb)?IrregularVerbPasts[verb]:(In(SemiRegularVerbPasts,verb)?SemiRegularVerbPasts[verb]:RegularVerbPasts[verb])
};

