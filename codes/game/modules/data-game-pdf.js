PageFormats={
	"Puzzlebook":function(data){
		var colophon=data.genre+" Puzzles by "+data.author;
		return{
			defaultFileName:colophon,
			pageSize: 'A4',
			pageOrientation:'portrait',
			pageMargins: [ 40, 60, 40, 60 ],
			pageBreakBefore:function(currentNode,followingNodesOnPage,nodesOnNextPage,previousNodesOnPage){
				return currentNode.headlineLevel===1;},
			footer:function(currentPage){
				if(currentPage>1)
					return [
					{	text:currentPage.toString(),
						alignment:"center"
					},
					{	text:colophon,
						alignment:"center",
						fontSize:8
					}]
			}
			// header:function(currentPage){
			// 	if(currentPage>1)
			// 		return [
			// 		{	text:currentPage.toString(),
			// 			alignment:"center"
			// 		},
			// 		{	text:colophon,
			// 			alignment:"left"
			// 		}]},
		}
	}
}

PdfCover=function(data){
	return [{
		text:data.genre,
		style:"header",
		fontSize:48
	},{
		image:First(PuzzlePictures).src,
		style:"picture",
	},{
		text:"Paper Puzzle Pack",
		style:"header"
	},{
		text:"by "+data.author,
		style:"header",
		italics:true
	},{
		text:DateString(data.date),
		style:"header",
		fontSize:20
	},{
		text:`You'll notice this autogenerated Puzzle Pack is still imperfect. Hopefully the quality will improve so that you too can generate your own Puzzle Pack effortlessly. That's the goal - so if this sounds interesting, or you have any special requirements or ideas please reach out!`,
		style:"colophon",
		margin:20,
		alignment:"center"
	}
	]
}

PdfPuzzlePage=function(metadata){
	return Join([{
		text:metadata.title,
		headlineLevel:1,
		style:"header",
	},{
		image:metadata.src,
		style:"picture"
	}],
	Range(1,metadata.difficulty).map(function(i){return {
		svg:IconSVGHTML(GlyphIcon("shards"))
	}}),
	[{
		text:DateString(metadata.date),
		style:"colophon"
	},{
		text:metadata.title+" by "+metadata.author,
		link:PageReSearch(PageAbsolute(metadata.url),metadata.href),
		style:"colophon"
	}])
};


PdfOptions=function(data){
	return {
		content:Prepend(
			Values(PuzzlePictures).map(puzzlePic=>PdfPuzzlePage(puzzlePic,data)),
			PdfCover(data)
		),
		styles:{
			"header":{
				color:"#333333",
				markerColor:"#333333",
				//lineHeight:4,
				fontSize:22,
				alignment:"center",
				bold:true
			},
			"colophon":{
				color:"#333333",
				markerColor:"#333333",
				alignment:"right",
				fontSize:14
			},
			"picture":{
				width:100,
				alignment:"center"
			}
		}
	}
}

MakePdf=function(){
	var options={
		author:"Pedro PSI",
		date:Today(),
		genre:"Bonsai Gardening"	
	};
	var format=Evaluate(PageFormats["Puzzlebook"],options);
	return pdfMake.createPdf(Group(PdfOptions(options),format));
}


